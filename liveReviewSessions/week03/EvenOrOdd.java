package week03;

public class EvenOrOdd {

    public static void main(String[] args) {
        int num1 = 78243;
        if (num1 % 2 == 0) {
            System.out.println(num1 + " is an even number.");
        }
        else  {
            System.out.println(num1 + " is an odd number.");
        }
    }


}
